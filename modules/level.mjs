const tiles = [];

const tileTypes = {
    1: {
        offX: 0,
        offY: 0,
        w: 10,
        h: 10,
        c: "black"
    },
    2: {
        offX: 3,
        offY: 2,
        w: 4,
        h: 6,
        c: "blue"
    }
};

function init() {
    for (let i = 0; i < level.length; i++) {
        for (let j = 0; j < level[i].length; j++) {
            if (level[i][j] !== 0) {
                const thisTile = tileTypes[level[i][j]];
                tiles.push(new Tile.Block(j * 10 + thisTile.offX, i * 10 + thisTile.offY, thisTile.w, thisTile.h, thisTile.c, level[i][j]));
            }
        }
    }
}

export { tiles, init }